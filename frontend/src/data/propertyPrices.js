export function getPricePerSqft(city, locality) {
  const cityKey = city.toLowerCase().trim();
  const localityKey = locality.toLowerCase().trim();

  // Try exact locality match first
  if (LOCALITY_PRICES[localityKey]) return LOCALITY_PRICES[localityKey];

  // Try partial locality match
  const partialMatch = Object.keys(LOCALITY_PRICES).find(
    (k) => localityKey.includes(k) || k.includes(localityKey)
  );
  if (partialMatch) return LOCALITY_PRICES[partialMatch];

  // Fallback to city base
  const cityMatch = Object.keys(CITY_BASE).find((k) => cityKey.includes(k));
  return cityMatch ? CITY_BASE[cityMatch] : 6000;
}
